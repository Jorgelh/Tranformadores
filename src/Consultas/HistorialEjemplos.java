     private void ListarTrabajosHistorial(){
        ArrayList<ClassTrabajos> result = InsertarEjemplos.ListarHistorialEjemplos(PN.getText());
        RecargarTabla(result);  
    }
     private void RecargarTabla(ArrayList<ClassTrabajos> list) {
         
              Object[][] datos = new Object[list.size()][9];
              int i = 0;
              for(ClassTrabajos t : list)
              {
                  datos[i][0] = t.getId();
                  datos[i][1] = t.getPN();
                  datos[i][2] = t.getJob();
                  datos[i][3] = t.getCliente();
                  datos[i][4] = t.getEstandar();
                  datos[i][5] = t.getRevision();
                  datos[i][6] = t.getComentarios();
                  datos[i][7] = t.getFecha();
                  datos[i][8] = t.getEstado();
                  i++;
              }    
             TaHistorial.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "No.","P/N","JOB","CLIENTE","ESTANDAR","REVISION","COMENTARIO","FECHA APROBACION","ESTADO"
 
             })
             {  
                 @Override
             public boolean isCellEditable(int row, int column){
                 return false;
             }
             });
             TableColumn columna1 = TaHistorial.getColumn("No.");
             columna1.setPreferredWidth(0);
             TableColumn columna2 = TaHistorial.getColumn("P/N");
             columna2.setPreferredWidth(15);
             TableColumn columna3 = TaHistorial.getColumn("JOB");
             columna3.setPreferredWidth(15);
             TableColumn columna4 = TaHistorial.getColumn("CLIENTE");
             columna4.setPreferredWidth(15);
             TableColumn columna5 = TaHistorial.getColumn("ESTANDAR");
             columna5.setPreferredWidth(50);
             TableColumn columna6 = TaHistorial.getColumn("REVISION");
             columna6.setPreferredWidth(25);
             TableColumn columna7 = TaHistorial.getColumn("COMENTARIO");
             columna7.setPreferredWidth(130);
             TableColumn columna8 = TaHistorial.getColumn("FECHA APROBACION");
             columna8.setPreferredWidth(75);

}
 
     private void ListarProcesos(){
        ArrayList<ClassTrabajos> result1 = InsertarEjemplos.ListarProcesoEjemplo(id);
        Listar(result1);  
    }
     private void Listar(ArrayList<ClassTrabajos> list1) {
         
              Object[][] datos = new Object[list1.size()][5];
              int i = 0;
              for(ClassTrabajos t : list1)
              {
                  datos[i][0] = t.getProceso();
                  datos[i][1] = t.getFecha();
                  datos[i][2] = t.getCantidad();
                  datos[i][3] = t.getComentarios();
                  datos[i][4] = t.getDepto();
                  i++;
              }    
             TablaPro.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "PROCESO","FECHA","CANTIDAD","COMENTARIO","DEPARTAMENTO"
             })
             {  
                 @Override
             public boolean isCellEditable(int row, int column){
             return false;
             }
             });
             TableColumn columna1 = TablaPro.getColumn("CANTIDAD");
             columna1.setPreferredWidth(0);
             TableColumn columna2 = TablaPro.getColumn("FECHA");
             columna2.setPreferredWidth(10);
             TableColumn columna3 = TablaPro.getColumn("PROCESO");
             columna3.setPreferredWidth(35);
     }
    
