    private void Buscar() {
        id_new = (Integer.parseInt(String.valueOf(trabajos.getModel().getValueAt(trabajos.getSelectedRow(), 0))));

        try {

            ClassTrabajos c = InsertTrabajosTransformadores.buscarTrabajoHistorial(Integer.parseInt(String.valueOf(trabajos.getModel().getValueAt(trabajos.getSelectedRow(), 0))));
            id = c.getId();
            PNINFO.setText(c.getPN());
            JOBINFO.setText(c.getJob());
            CLIENTEINFO.setText(c.getCliente());
            REVISIONINFO.setText(c.getRevision());
            ESTANDAR.setText(c.getEstandar());
            FECHAIN.setText(c.getFecha());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error =  " + e);
        }
    }
    
