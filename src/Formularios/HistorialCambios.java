    private void TablaCambiosRealizados(ArrayList<ClassTrabajos> list) {

        Object[][] datos = new Object[list.size()][5];
        int i = 0;
        for (ClassTrabajos t : list) {
            datos[i][0] = t.getId();
            datos[i][1] = t.getIntegridadS();
            datos[i][2] = t.getCambio();
            datos[i][3] = t.getAprueba();
            datos[i][4] = t.getFecha();
            i++;
        }
        CAMBIOS.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                    "","INTEGRIDAD","CAMBIO HECHO","APRUEBA","FECHA AUTORIZACION"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
        }
        });
        TableColumn columna1 = CAMBIOS.getColumn("");
        columna1.setPreferredWidth(-50);
        TableColumn columna2 = CAMBIOS.getColumn("INTEGRIDAD");
        columna2.setPreferredWidth(0);
        TableColumn columna3 = CAMBIOS.getColumn("CAMBIO HECHO");
        columna3.setPreferredWidth(300);
        TableColumn columna4 = CAMBIOS.getColumn("APRUEBA");
        columna4.setPreferredWidth(20);
        TableColumn columna5 = CAMBIOS.getColumn("FECHA AUTORIZACION");
        columna5.setPreferredWidth(50);
    }   
    
    
    
    

