    private void Buscar() {
        id_new = (Integer.parseInt(String.valueOf(trabajos.getModel().getValueAt(trabajos.getSelectedRow(), 0))));

        try {

            ClassTrabajos c = InsertTrabajosTransformadores.buscarTrabajo(Integer.parseInt(String.valueOf(trabajos.getModel().getValueAt(trabajos.getSelectedRow(), 0))));
            id = c.getId();
            PNINFO.setText(c.getPN());
            JOBINFO.setText(c.getJob());
            CLIENTEINFO.setText(c.getCliente());
            REVISIONINFO.setText(c.getRevision());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error =  " + e);
        }
    }

    private void insertarCambio() {

        

            try {
                ClassTrabajos t = new ClassTrabajos();
                t.setId(id_new);
                t.setProposito(propo);
                t.setConsecuencia(consecuencia);
                t.setIntegridad(integridad.getText());
                t.setR_foto(r_foto);
                t.setR_materia(r_materia);
                t.setR_imprimir(r_impre);
                t.setSolicita(deptosolicita);
                t.setAprueba(Integer.parseInt(aprueba.getText()));
                t.setFecha1(fecha.getDate());
                t.setCambio(cambio.getText());
                t.setObservaciones(observaciones.getText());
                InsertTrabajosTransformadores.InsertarCambio(t);
                JOptionPane.showMessageDialog(null, "Agregado...");
                ListarCambios();
                limpiar();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "error" + e);
            }
        
    }
    
    
