    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
    
        
        if (id_new != 0 && consecuencia != 0 && fecha.getDate() != null && propo != 0 && deptosolicita != 0 && aprueba.getText().compareTo("") !=0) {
        
        try {

            Connection con = BD_RECURSOS.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select COUNT(codigo) from alistaempleados where codigo=" + aprueba.getText());
            rs.next();
            int c = rs.getInt("count(codigo)");
            if (c == 0) {
                JOptionPane.showMessageDialog(null, "EL CODIGO EMPLEADO NO EXITE INTENTE DE NUEVO...");
                aprueba.setText("");aprueba.requestFocus();

            } else {

                if (foto.isSelected()) {
                    r_foto = 1;
                } else {
                    r_foto = 0;
                }
                if (materia.isSelected()) {
                    r_materia = 1;
                } else {
                    r_materia = 0;
                }
                if (impresion.isSelected()) {
                    r_impre = 1;
                } else {
                    r_impre = 0;
                }
                insertarCambio();

            }
        } catch (Exception e) {
            System.out.println("ERROR " + e);
        }

 
    } else {
            JOptionPane.showMessageDialog(null, "Ingrese Todos los Datos");
        }    
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

